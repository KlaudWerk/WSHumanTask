using System;
using KlaudWerk.Common;

namespace KlaudWerk.Security
{
    /// <summary>
    /// Type-safe Identity Id
    /// </summary>
    [Serializable]
    public class IdentityId:GuidId<IdentityId>, IEquatable<IdentityId>

    {
        /// <summary>
        /// Initializes a new instance of the <see cref="IdentityId"/> class.
        /// </summary>
        /// <param name="id">The id.</param>
        public IdentityId(Guid id) : base(id)
        {
        }

        /// <summary>
        /// Initializes a new instance of the <see cref="IdentityId"/> class.
        /// </summary>
        /// <param name="id">The id.</param>
        public IdentityId(string id):base(new Guid(id))
        {
        }

        /// <summary>
        /// Initializes a new instance of the <see cref="IdentityId"/> class.
        /// </summary>
        public IdentityId()
        {
        }

        /// <summary>
        /// Returns a <see cref="System.String"/> that represents this instance.
        /// </summary>
        /// <returns>
        /// A <see cref="System.String"/> that represents this instance.
        /// </returns>
        public override string ToString()
        {
            return Id.ToString();
        }

        /// <summary>
        /// Implements the operator ==.
        /// </summary>
        /// <param name="left">The left.</param>
        /// <param name="right">The right.</param>
        /// <returns>The result of the operator.</returns>
        public static bool operator ==(IdentityId left, IdentityId right)
        {
            return Equals(left, right);
        }

        /// <summary>
        /// Implements the operator !=.
        /// </summary>
        /// <param name="left">The left.</param>
        /// <param name="right">The right.</param>
        /// <returns>The result of the operator.</returns>
        public static bool operator !=(IdentityId left,IdentityId right)
        {
            return !Equals(left, right);
        }


        /// <summary>
        /// Indicates whether the current object is equal to another object of the same type.
        /// </summary>
        /// <returns>
        /// true if the current object is equal to the <paramref name="other"/> parameter; otherwise, false.
        /// </returns>
        /// <param name="other">An object to compare with this object.</param>
        public bool Equals(IdentityId other)
        {
            return this==other;
        }

        /// <summary>
        /// Determines whether the specified <see cref="System.Object"/> is equal to this instance.
        /// </summary>
        /// <param name="obj">The <see cref="System.Object"/> to compare with this instance.</param>
        /// <returns>
        /// 	<c>true</c> if the specified <see cref="System.Object"/> is equal to this instance; otherwise, <c>false</c>.
        /// </returns>
        public override bool Equals(object obj)
        {
            if (ReferenceEquals(null, obj)) return false;
            if (ReferenceEquals(this, obj)) return true;
            return Equals(obj as IdentityId);
        }

        /// <summary>
        /// Returns a hash code for this instance.
        /// </summary>
        /// <returns>
        /// A hash code for this instance, suitable for use in hashing algorithms and data structures like a hash table. 
        /// </returns>
        public override int GetHashCode()
        {
            return base.GetHashCode();
        }
    }
}
